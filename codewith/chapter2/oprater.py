
# 1. Arithmetic Operators
a=3
b=4
print(a+b) # Addition

# assignment operator
a+=b
print(a) # 7
# 2. Comparison Operators
a=3
b=4
print(a==b) # False
print(a!=b) # True
print(a<b) # True
print(a>b) # False
print(a<=b) # True
print(a>=b) # False
# 3. Logical Operators
a=True
b=False
print(a and b) # False
print(a or b) # True
print(not a) # False
# 4. Identity Operators
a=3
b=3
print(a is b) # True
print(a is not b) # False
# 5. Membership Operators
a=[1,2,3]
b=1
print(b in a) # True
print(b not in a) # False
# 6. Bitwise Operators
a=3
b=4
print(a&b) # 0
print(a|b) # 7
print(a^b) # 7
print(~a) # -4
print(a<<2) # 12
print(a>>2) # 0
